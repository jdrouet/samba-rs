#[test]
fn should_parse_msg_00_req_negotiate() {
    use samba_rs::entities::v1::header::{Command, ExtendedFlags, Flags};

    let netbios = samba_rs::entities::netbios::Header::parse(&MSG_00_REQ_NEGOTIATE[0..4]).unwrap();
    assert_eq!(netbios.length(), 69);
    let header = samba_rs::entities::header::Header::parse(&MSG_00_REQ_NEGOTIATE[4..]).unwrap();
    let samba_rs::entities::header::Header::V1(header) = header else {
        panic!("expected v1");
    };

    assert_eq!(header.command, Command::Negotiate);
    assert_eq!(header.status, 0);
    assert_eq!(header.flags, Flags::SMB_FLAGS_CASE_INSENSITIVE);
    assert_eq!(
        header.extended_flags,
        ExtendedFlags::SMB_FLAGS2_UNICODE
            .union(ExtendedFlags::SMB_FLAGS2_NT_STATUS)
            .union(ExtendedFlags::SMB_FLAGS2_EXTENDED_SECURITY_NEGOTIATION)
            .union(ExtendedFlags::SMB_FLAGS2_LONG_NAMES)
    );
    assert_eq!(header.pid_high, 0);
    assert_eq!(header.security_features, [0u8; 8]);
    assert_eq!(header.pid_low, 1);
    assert_eq!(header.tree_id, 65535);
    assert_eq!(header.user_id, 65535);
    assert_eq!(header.multiplex_id, 0);
}

